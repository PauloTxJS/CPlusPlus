#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    int n1 = 10;
    int n2 = 2;
    int soma = n1 + n2;
    int diferenca = n1 - n2;
    int produto = n1 * n2;
    int quociente = n1 / n2;
    int modulo = n1 % n2;

    cout << soma << endl;
    cout << diferenca << endl;
    cout << produto << endl;
    cout << quociente << endl;
    cout << modulo << endl;
    
    return 0;
}
